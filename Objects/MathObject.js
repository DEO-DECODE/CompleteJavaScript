// Math object built in JavaScript.
let number = 9.6;
console.log(Math.round(number)); //Round to nearest integer
console.log(Math.floor(number)); //Round to down
console.log(Math.ceil(number)); //Round to up
console.log(Math.trunc(number)); //Remove decimal part.
let random = Math.random(); //Produces results between 0 to 1
console.log(Math.round(random * 100));
Math.abs(-4);//Absolute value k liye
Math.pow(2,3);//2^3
Math.sqrt(16);//Square root de dega
Math.max(4,5,100,0,1);
Math.min(4,5,100,1);
